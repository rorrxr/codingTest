# 정수 입력
N = int(input())

# 모스부호 입력
morse = input().split()

# 결과 저장 변수 초기화
result = ""

# 입력된 morse 문자열 길이까지 for문 실행
for m in morse:
    # 딕셔너리에서 모스부호를 찾아서 문자를 count
    result += morse_code[m]

# 결과값 출력
print(result)