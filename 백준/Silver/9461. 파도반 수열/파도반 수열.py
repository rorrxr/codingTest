"""
문제
오른쪽 그림과 같이 삼각형이 나선 모양으로 놓여져 있다. 첫 삼각형은 정삼각형으로 변의 길이는 1이다. 그 다음에는 다음과 같은 과정으로 정삼각형을 계속 추가한다. 나선에서 가장 긴 변의 길이를 k라 했을 때, 그 변에 길이가 k인 정삼각형을 추가한다.

파도반 수열 P(N)은 나선에 있는 정삼각형의 변의 길이이다. P(1)부터 P(10)까지 첫 10개 숫자는 1, 1, 1, 2, 2, 3, 4, 5, 7, 9이다.

N이 주어졌을 때, P(N)을 구하는 프로그램을 작성하시오.

입력
첫째 줄에 테스트 케이스의 개수 T가 주어진다. 각 테스트 케이스는 한 줄로 이루어져 있고, N이 주어진다. (1 ≤ N ≤ 100)

출력
각 테스트 케이스마다 P(N)을 출력한다.

예제 입력 1:
2
6
12

예제 출력 1:
3
16
"""

import sys
input = sys.stdin.readline

T = int(input())  # 입력 받은 테스트 케이스의 개수를 저장

# 파도반 수열을 한 번 계산해 두고, 모든 테스트 케이스에서 바로 사용할 수 있도록 준비
dp = [0] * 101  # 최대 N은 100이므로 101 크기의 배열을 생성
dp[1] = 1  # 파도반 수열의 첫 번째 값
dp[2] = 1  # 파도반 수열의 두 번째 값
dp[3] = 1  # 파도반 수열의 세 번째 값
dp[4] = 2  # 파도반 수열의 네 번째 값
dp[5] = 2  # 파도반 수열의 다섯 번째 값
dp[6] = 3  # 파도반 수열의 여섯 번째 값

# 7번째 값부터는 규칙에 따라 dp 배열을 채워 나가기
for i in range(7, 101):  # 7부터 100까지 반복
    dp[i] = dp[i - 1] + dp[i - 5]  # dp[i] = dp[i-1] + dp[i-5]

for _ in range(T):  # T개의 테스트 케이스에 대해 반복
    N = int(input())  # 각 테스트 케이스마다 N 값을 입력받음
    print(dp[N])  # N번째 파도반 수열 값 출력
