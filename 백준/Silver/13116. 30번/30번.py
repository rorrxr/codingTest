# 가장 가까운 공통 조상을 찾는 문제
# # 완전 이진 트리의 특징과 주어진 예제에 따라 각 노드 번호는 정수로 주어지며, 트리의 공통 조상을 찾습니다.

# 부모 노드는 node//2로 계산됩니다.
# 두 노드가 같아질 때까지 반복해서 각 노드를 위로 이동시킵니다.

# 그 다음 가장 가까운 공통 조상을 출력합니다.

import sys
input = sys.stdin.readline

# 테스트 케이스 수 입력
t = int(input().strip())

# 결과 저장용 리스트
results = []

# 테스트 케이스 반복 처리
for _ in range(t):
    a, b = map(int, input().split())

    # 두 노드가 같아질 때까지 반복
    while a != b:
        if a > b:
            a //= 2
        else:
            b //= 2

    # 공통 조상을 결과 리스트에 추가
    results.append(a * 10)  # 문제 조건에 따라 10을 곱한 값을 출력

# 결과 출력
for result in results:
    print(result)
