n = int(input())  # 사용자로부터 N 입력받기
count = 0         # 찾은 종말의 수 개수
i = 666           # 종말의 수는 666부터 시작

# N번째 종말의 수를 찾을 때까지 반복
while count < n:
    # 현재 숫자에 '666'이라는 연속된 숫자가 포함되어 있는지 확인
    if '666' in str(i):
        count += 1  # 포함되어 있다면 카운트 증가
    i += 1  # 다음 숫자로 이동

# 마지막 i는 N번째를 초과한 수이므로, N번째 종말의 수는 i - 1
print(i - 1)